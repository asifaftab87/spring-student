package org.la.student.jamal.loob;

public class ForLoob51 {
	
	//51.	Wap to find sum of all natural numbers between 1 to n.
	
	public static void main(String[] args) {
		

		int i=1;
		for (;i<='n';i++) {
			System.out.println(i*(i+1)/2 +" this is the sum of "+i );
			
		}
		
		System.out.println("--------------------------------------------------------------");
		
		int j=1;
		while (j<='n') {
			System.out.println(j*(j+1)/2 + "this is while sum of " +j);
			j++;
		}
		
		System.out.println("----------------------------------end---------------------------");
		
	}

}
