package org.la.student.jamal.loob;

public class ForLoob12 {
	//12.	wap to print 15 times hello.

	public static void main(String[] args) {
		


		for (int i=0; i<15; i++) {
			System.out.println("hello");
		}
		int j=0;
		while (j<15) {
			System.out.println(j + "-this is while hello");
			j++;
		}
		

	}

}
