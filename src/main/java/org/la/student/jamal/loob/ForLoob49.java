package org.la.student.jamal.loob;

public class ForLoob49 {
	
	//49.	Wap to print all even numbers between 1 to 10
	
	public static void main(String[] args) {
		

		for ( int i=1; i<=100;i++) {
			if (i%2==0) {
				System.out.println(i);
			}
		}
		
		int j=1;
		while (j<=100) {
			if (j%2==0) {
				System.out.println("this is while :" +j);
			}
			j++;
		}
		
		
	}

}
