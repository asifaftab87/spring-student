package org.la.student.jamal.loob;

public class ForLoob48 {
	
	//48.	Wap to print all alphabets from a to z.
	
	public static void main(String[] args) {
		

		char i='a';
		for (;i<='z';i++) {
			System.out.println(i);
		}
		char j='a';
		while (j<='z') {
			System.out.println("this is while : " +j);
			j++;
		}
	}

}
