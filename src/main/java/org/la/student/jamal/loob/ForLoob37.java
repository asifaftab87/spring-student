package org.la.student.jamal.loob;

public class ForLoob37 {
	//37.	wap to print all numbers from 1 to 10 except 3 and 7.

	public static void main(String[] args) {
		
		for (int i=1; i<=10; i++) {
			if (( i!=3)&&(i!=7)) {
				System.out.println(i);
			}
		}
		
		int j=1;
		while (j<=10) {
			if ((j!=3)&&(j!=7)) {
				System.out.println("this is while :" +j);
				
			}
				j++;
		}
		
		
			
		
		



		

	}

}
