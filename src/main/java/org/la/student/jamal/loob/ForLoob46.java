public class ForLoob46 {
	
	//46.	Wap to print all natural numbers from 1 to n.
	
	public static void main(String[] args) {
		

		int i=1;
		for(;i<='n';i++) {
			System.out.println(i);
		}

		int j =1;
		while (j<='n') {
			System.out.println("this is while : "+ j);
			j++;
		}
	}

}
