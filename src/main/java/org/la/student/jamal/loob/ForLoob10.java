public class ForLoob10 {
	//10.	wap to print 5 times A.

	public static void main(String[] args) {
		
		for (int i=0; i<5; i++) {
			System.out.println("A");
		}
		
		int j=0;
		while (j<5) {
			System.out.println("this is while : A ");
			j++;
			
		}
			
		

	}

}
