public class loopQ72 {
	public static void main(String[] args) {
		
		 
		  int n=6;
		  int sum=0;
		  for(int i=1;i<=n/2;i++) {
			  
			  if(n%i==0) {
				  
				  sum+=i;
			  }
		  }
			  if(sum==n) {
				  
				  System.out.println("it is a perfect number");
			  }
			  else {
				  System.out.println("it is NOT a perfect number");
			  }
		  

	}   
	}
		
	
		
		
		
		
		
		
		
		
		
		
	

