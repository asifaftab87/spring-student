package org.la.student.muhammad.abdullahi.ternary;

public class loopQ87 {
	public static void main(String[] args) {
		
		 
		
  

	    
		int n = 5;
		for(int i=-n ; i<=n ; i++) {

			int j=i;
			if(j<0){
				j = -j;
			}

			//printing space
			for(int k=1 ; k<=j ; k++) {
				System.out.print(" ");
			}
			//printing star
			for(int k=0 ; k<=n-j ; k++) {
				System.out.print("5 ");
			}
			System.out.println("");
		}
	}
	}
		
	
		
		
		
		
		
		
		
		
		
		
	

