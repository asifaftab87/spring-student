package org.la.student.sadique;

public class ForLoop19 {

	public static void main(String[] args) {
		// 19.	wap to print all numbers between 30 to 80 those are divisible by 6 and 5.
		
		for(int a=30;a<=80;a++) {
			if(a%5==0) {
				System.out.println(a);
			} if(a%6==0) {
				System.out.println(a);
			}
		}

	}

}
