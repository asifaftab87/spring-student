package org.la.student.sadique.loop;
//wap to print 5 times A.
public class PrintNTimes {

	public static void main(String[] args) {
		int a;
		for(a=1;a<=10;a++) {
			if(a==6)
				break;
				System.out.println(a+" Times A");
		}System.out.println(" ");
		//wap to print 1 time a.
		for(a=1;a<=10;a++) {
			if(a==2)
				break;
				System.out.println(a+" Times a");
		}System.out.println(" ");
		//	wap to print 15 times hello.
		for(a=1;a<=20;a++) {
			if(a==16)
				break;
				System.out.println(a+" Times hello");
		}System.out.println(" ");
		// wap to print 10 times wolrd.
		for(a=1;a<=20;a++) {
			if(a==11)
				break;
				System.out.println(a+" Times World");
		}
	}

}
