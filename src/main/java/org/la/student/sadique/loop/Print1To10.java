package org.la.student.sadique.loop;
//wap to print number from 1 to 10
public class Print1To10 {
	public static void main(String[] args) {
		int a;
		System.out.println("From 1 To 10  ");
		System.out.println(" ");
		for(a=1;a<=10;a++) {
			System.out.println(a);
		}
		//wap to print number from 10 to 1.
		System.out.println(" ");
		System.out.println("In Reverse Order From 10 To 1  ");
		for(a=10;a>=1;a--) {
			System.out.println(a);
		}
		//wap to print to print all negative numbers between -1 and -10.
		System.out.println(" ");
		System.out.println("Negative Number Form -1 to -10  ");
		for(a=-1;a>=-10;a--) {
			System.out.println(a);
		}

	}

}
