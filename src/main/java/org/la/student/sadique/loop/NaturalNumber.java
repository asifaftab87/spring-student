package org.la.student.sadique.loop;
//Wap to print all natural numbers from 1 to n.
public class NaturalNumber {
	public static void main(String[] args) {
		int n;
		System.out.println("Natural number ");
		for(n=1;n<=10;n++) {
			System.out.print(" "+n+" ");
			
		}System.out.println(" ");
		System.out.println("Natural number in reverse order");
		for(n=10;n>=1;n--) {
			System.out.print(" "+n+" ");
		}

	}

}
