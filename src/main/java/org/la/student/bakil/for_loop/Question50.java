/*
 * Wap to find sum of all natural numbers between 1 to n.
 */
public class Question50 {

	public static void main(String[] args) {
	

		 int n=12,i,sum=0;
		 
		 for(i=0;i<=n;i++)
		 {
		 sum = sum + i;
		 }  
		 
		 System.out.println("Sum of first "+n+" natural  = "+sum);
		    }

}
