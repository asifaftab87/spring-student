package org.la.student.kayes.methods;

public class VarientValue {
	private int proId;
	private int optId;
	private int valId;
	private int dimId;
	private int varId;
	private int price;
	
	public int getPro()
	{
		return proId;
	}
	public void setPro(int p)
	{
		proId=p;
	}
	
	public int getOpt()
	{
		return optId;
	}
	public void setOpt(int o)
	{
		optId=o;
	}
	
	public int getVal() 
	{
		return valId;
	}
	public void setVal(int v)
	{
		valId=v;
	}
	
	public int getDim() 
	{
		return dimId;
	}
	public void setDim(int d)
	{
		dimId=d;
	}
	
	public int getVar() 
	{
		return varId;
	}
	public void setVar(int va)
	{
		varId=va;
	}
	
	public int getPri() 
	{
		return price;
	}
	public void setPri(int p)
	{
		price=p;
	}

}
