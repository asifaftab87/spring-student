package org.la.student.kayes.method;

public class Q17Average {
	public static void main(String[] args) {
		int a=1;
		int b=2;
		int c=5;
		double s=avg(a,b,c);
		System.out.println("average=	"+s);

	}
	static double avg(int n1,int n2,int n3)
	{
		double k;
		k=(double)(n1+n2+n3)/3.0;
	
	return k;
	}

}
