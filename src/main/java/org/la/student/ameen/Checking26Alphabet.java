package org.la.student.ameen;


public class Checking26Alphabet {

	public static void main(String[] args) {
		// All alphabet from a to z using while loop
		
		char alpha = 'a' ;
		System.out.println("All 26 alphabet ");
		
		while(alpha <= 'z')
		{
			System.out.println( "alpha : " +alpha);
			alpha++;
		}
		
		
		

	}

}
