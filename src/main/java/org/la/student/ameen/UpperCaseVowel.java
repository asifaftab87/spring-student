package org.la.student.ameen;

public class UpperCaseVowel {

	public static void main(String[] args) {
		// wap to print all upper case vowels.
		char ch ='A';
		while(ch <='Z')
		{
			if(ch=='A' || ch=='E' || ch=='I' || ch=='O' || ch=='U')
			{
				System.out.println("ch : " +ch);
			
			}
			ch++;
		}
		

	}

}
