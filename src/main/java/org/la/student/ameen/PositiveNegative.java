public class PositiveNegative {

	public static void main(String[] args) {
		int num = 0 ;
		if(num > 0)
		{
			System.out.println("it is positive");
		}
		else if(num == 0)
		{
			System.out.println("it is zero");
		}
		else
		{
			System.out.println("it is negative");
		}

	}

}
