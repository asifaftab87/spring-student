package org.la.student.ameen;

public class PatternPrintingofNumber {

	public static void main(String[] args) {
		// Pattern Printing of Number
		int num = 3;
		for(int i = 1;i<=num;i++)
		{
			for(int k = 1;k<=i;k++)
			{
				System.out.print( +i +" " );
			}
			System.out.println("  ");
		}

	}

}
