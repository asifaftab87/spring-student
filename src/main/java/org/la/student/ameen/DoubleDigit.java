package org.la.student.ameen;

public class DoubleDigit {

	public static void main(String[] args) {
		int num = -19;
		if( num >= 10 && num < 100)
		{
			System.out.println(" num is double");
			}
		else if(num <= -10 && num > -100)
		{
			System.out.println("num is double");
		}
		else
		{
			System.out.println(" num is not double");
		}

	}

}
