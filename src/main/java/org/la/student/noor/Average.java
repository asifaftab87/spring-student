package org.la.student.noor;

public class Average {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		int avg=(a+b)/2;
		
		System.out.println("Average of two nos is:" +avg);
		
		int x=11;
		int y=20;
		int z=32;
		int avg1=(x+y+z)/3;
		
		System.out.println("Average of three nos is:" +avg1);
		
		int p=11;
		int q=10;
		int r=23;
		int s=24;
		int avg2=(p+q+r+s)/4;
		
		System.out.println("Average of four nos is:" +avg2);
		

	}

}
