//wap to print zero if number zero, 1 if positive and -1 if number is negative
public class IfElse17 {

	public static void main(String[] args) {
		int num=0;
		
		 if(num>0) {
			 System.out.println("1");
		 }
		 if(num<0) {
			 System.out.println("-1");
		 }
		 if(num==0)
		 {
			 System.out.println("zero");
		 }

	}

}
