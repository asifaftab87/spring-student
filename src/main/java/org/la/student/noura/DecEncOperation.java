package org.la.student.noura;

public class DecEncOperation {

	public static void main(String[] args) {


		int a = -3;
		int b = ++a;
		System.out.println("a="+a);
		System.out.println("b="+b);
		
		
		int c = 3;
		int d = ++c + 9;
		System.out.println("c="+c);
		System.out.println("d="+d);

	}

}
