package org.la.student.noura;

public class If {

	public static void main(String[] args) {


		//java program to print single if number is single digit
		
		int n = -10;
		if( n >= 0 && n < 10)
		{
			System.out.println(" number is single digit");
			}
		else if(n > -10 && n < -1)
		{
			System.out.println("number is single digit");
		}
		else
		{
			System.out.println(" number is not single digit");
		}

	}

}
