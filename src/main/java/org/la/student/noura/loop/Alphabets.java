package org.la.student.noura.loop;

public class Alphabets {

	public static void main(String[] args) {

		
		//Wap to print all alphabets from a to z.
		// while loop
		
		char alpha='a';
		while(alpha<='z') {
			System.out.print(alpha+" ");
			alpha++;
		}
		System.out.println("            ");
		//for loop
		char a='A';
		for(;a<='Z';) {
			System.out.print(a+" ");
			a++;
		}

	}

}
