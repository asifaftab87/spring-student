package org.la.student.noura.loop;

public class WhileLoop3 {

	public static void main(String[] args) {

		
	//wap to print all lower case alphabets
		
		char alpha ='a';
		while(alpha<='z') {
			
			System.out.println(alpha);
			alpha++;
		}
		
	//or 
		
		int b=97;
		char c=(char)b;
		while(c>=97 && c<=122) {
			System.out.print(c);
		c++;}
	}

}
