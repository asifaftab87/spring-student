package org.la.student.noura.loop;

public class SumNaturalNum {

	public static void main(String[] args) {
		//	Wap to find sum of all natural numbers between 1 to n.

		
		//using while
		int num=100 , sum = 0, i=1;

	        while ( i<=num)
	        {
	        
	            sum= sum +i;
	            i++;
	            
	        }

	        System.out.println("Sum = " + sum);
	        
	        
	        
	        
	    }


}
