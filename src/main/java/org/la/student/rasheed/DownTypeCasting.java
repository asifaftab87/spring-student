package org.la.student.rasheed;

public class DownTypeCasting {

	public static void main(String[] args) {
		
		int a = 12;
		double b =3.4;
		
		int k = (int) (a * b);
		
		System.out.println(k);
		
		int x = 5/10;
		System.out.println(x);
		
		double f = (double) 10/20;
		
		System.out.println(f);

	}

}
