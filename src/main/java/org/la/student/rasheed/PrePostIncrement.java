package org.la.student.rasheed;

public class PrePostIncrement {

	public static void main(String[] args) {
		
		
		int i=4;
		int k=++i;
		
		System.out.println("i: "+i);
		System.out.println("k: "+k);
		
		
		int x =-1;
		int y =x++;
		
		System.out.println("x: "+x);
		System.out.println("y: "+y);
		
		

	}

}
