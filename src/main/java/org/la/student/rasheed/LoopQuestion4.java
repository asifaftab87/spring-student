package org.la.student.rasheed;
/*
 * wap to print first five even numbers.
 */

public class LoopQuestion4 {

	public static void main(String[] args) {
		
		int i = 1;
		int n =4;
		
		while (i<=5) {
			int prod =n*i;
			System.out.println(n+" * "+i+" = "+prod);
			++i;
		}
		
		System.out.println("---- end ----");

	}

}
