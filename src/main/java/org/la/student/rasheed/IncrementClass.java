package org.la.student.rasheed;

public class IncrementClass {

	public static void main(String[] args) {
		
		int a = 3;
		a++;
		
		System.out.println(a);
		
		a++;
		System.out.println(a);
		
		int b =-15;
		
		b++;
		System.out.println(b);
		
		int c =-1;
		
		c++;
		System.out.println(c);
		
		
	}

}
