package org.la.student.rasheed;

/*
 * wap to print numbers which is multiple of 3 between 1 to 22.
 */

public class FactorialWhileQ9 {

	public static void main(String[] args) {
	
		int f =1;
		int n =3;
		
		while(n>=1) {
			f = f * n;
			n--;
			
		}
		System.out.println("Factorial: "+f);
		

	}

}
