package org.la.student.barry.loop.task;
public class Loop66 {
	
	//Wap to find power of a number using for loop.
	public static void main(String[] args) {
		
		{  
			 int base = 2, exponent = 5;

			    long result = 1;

			    while (exponent != 0) {
			      result *= base;
			      --exponent;
			    }

			    System.out.println("Answer = " + result);
     }
   }
}
	
  

	
	
