package org.la.student.barry.clas.task;

public class Stuff {
	/*
	 * Create a class Stuff having member variables id, stuffName, thumbnails. create getters and 
		setters for all member variables. Create object of Stuff class in other class inside 
		main method and set all member values and print all member values using getters.
		
	 */
	private int id;
	private String stuffName;
	private String thumbNails;
	
	public int getId() {
		return id;
	
	}
	public void setId(int i) {
		id=i;
	}
	
	
	public String getStuffName() {
		return stuffName;
	
	}
	public void setStuffName(String n) {
		stuffName=n;
	}
	public String getThumbNails() {
		return thumbNails;
	
	}
	public void setThumbNails(String s) {
		thumbNails=s;
	}
	


}
