package org.la.student.tahir;

public class Typecasting {

	public static void main(String[] args) {
		char c = 'a';
		int i = c;
		System.out.println(i);

		int z = 97;
		char d= (char)z;
		System.out.println(d);
		
		int a = 19/5;
		System.out.println(a);
		
		char e = 65;
		System.out.println(e);
		
		int s = 'a';
		System.out.println(s);
		
		
	}

}
