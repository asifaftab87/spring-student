package org.la.student.tahir;

public class Control_Statement_40 {

	public static void main(String[] args) {
		//  Wap to check if a given alphabet is lowercase(a to z) or uppercase(A to Z)

			char c = 's';
			if (c>='A'&&c<'Z') {
				
				System.out.println(c + " is an alphabet and uppercase");
			}
			else if ( c>='a' &&  c<='z'){
				
				System.out.println(c + " is an alphabet and lowercase");
			}
	}

}
