	// Perimeter of a rectangle
	public static void main(String[] args) {
		
		double L =3;
		double W = 4;
		
		System.out.println("The Perimeter of the rectangle is: " + P(L,W));
	
		
		
	}
	
	
	static double P (double a, double b) {
		
		
		double A= 2*(a+b);
		return A;
		
	}
	

	

