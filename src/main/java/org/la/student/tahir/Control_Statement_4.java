package org.la.student.tahir;

public class Control_Statement_4 {

	public static void main(String[] args) {
		// wap to find whether a number is divisible by a given number completely.
		
		int c = 22;
		System.out.println("check if " + c +" is divisible by 3");
		
		if (c%3 == 0) {
			System.out.println(c + " is divisible by 3");
		}
		
		else {
			System.out.println(c + " is not divisible by 3");
		}

	}

}
