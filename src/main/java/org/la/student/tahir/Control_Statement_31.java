public class Control_Statement_31 {

	public static void main(String[] args) {
		// wap to print zero if number zero, 1 if positive and -1 if number is negative
		
		int a =-2;
		
		if (a==0) {
			System.out.println( "0");
		}
		else if (a>0) {
			System.out.println("1");
		}
		else {
			System.out.println("-1");
		}
	}

}
