package org.la.student.sajid;

public class ControlStatementWap48 {
	public static void main(String[] args) {
		int a=55;
		int b=6;
		int c=56;
		if(a<b && a<c)
			System.out.println("A is least Number "+a);
		else if(b<a && b<c)
			System.out.println("B is least Number "+b);
		else if(c<a && c<b)
			System.out.println("C is least Number "+c);
	}

}
