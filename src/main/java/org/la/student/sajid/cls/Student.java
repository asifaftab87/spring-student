package org.la.student.sajid.cls;

public class Student {
	private int rollNumber;
	private String name;
	
	public int getRollNumber() {
		return rollNumber;
	}
	
	public void setRollNumber(int i) {
		rollNumber=i;
	}
	
	public String getName() {
		return name;
		
	}
	public void setName(String n) {
		name=n;
	}
	
	

}
