package org.la.student.asif;

public class IfOne5 {

	public static void main(String[] args) {
	
		int p = 45;
		
		if(p>=60) {
			System.out.println("1st");
		}
		
		if(p>=50 && p<60) {
			System.out.println("2nd");
		}
		
		if(p>=40 && p<=49) {
			System.out.println("3rd");
		}
		
		if(p<40) {
			System.out.println("fail");
		}
		
	}
	
}
